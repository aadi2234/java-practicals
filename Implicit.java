class Implicit
{
	public static void main(String args[])
	{
         System.out.println("Implicit Conversion:");
        byte v=8;
        int w=v;
        System.out.println(w);
		int a=5;
		double i=a;
		System.out.println(i);
        int b=14;
        Float j=(float)b;
        System.out.println(j);
        int c=5000001;
        long k=c;
        System.out.println(k);
	}
}